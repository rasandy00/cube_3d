#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	if (!s)
		return (NULL);
	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] != (char)c)
			i--;
		else
			return ((char *)&s[i]);
	}
	return (NULL);
}
